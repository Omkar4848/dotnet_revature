using System;

public class PrimitiveTypesDemo
{
  public static void Run()
  {
    Console.WriteLine("\n--- Primitive Types ---");

    int number = 10;
    double price = 99.99;
    char grade = 'A';
    bool isActive = true;

    Console.WriteLine(number);
    Console.WriteLine(price);
    Console.WriteLine(grade);
    Console.WriteLine(isActive);
  }
}
